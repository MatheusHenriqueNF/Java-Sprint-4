package br.com.fiap.model;

public class EstacaoLinhaModel {
    private Long idEstacaoLinha;
    private Long idEstacoes;
    private Long idLinha;

    public Long getIdEstacaoLinha() { return idEstacaoLinha; }
    public void setIdEstacaoLinha(Long idEstacaoLinha) { this.idEstacaoLinha = idEstacaoLinha; }

    public Long getIdEstacoes() { return idEstacoes; }
    public void setIdEstacoes(Long idEstacoes) { this.idEstacoes = idEstacoes; }

    public Long getIdLinha() { return idLinha; }
    public void setIdLinha(Long idLinha) { this.idLinha = idLinha; }
}
